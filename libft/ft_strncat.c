#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	a;
	size_t	b;

	a = 0;
	b = 0;
	while (s1[a] != '\0')
	{
		a++;
	}
	while ((n > 0) && s2[b])
	{
		s1[a + b] = s2[b];
		b++;
		n--;
	}
	s1[a + b] = '\0';
	return (s1);
}
